let sigla = prompt("Insira a sigla de eum estado brasileiro")

switch(sigla){
    case "SP" :
        alert("São Paulo")
        break
    case "RJ":
        alert("Rio de Janeiro")
        break
    case "ES":
        alert("Espírito Santo")
        break
    case "MG":
        alert("Minas Gerais")
        break
    default:
        alert("Estado não encontrado")
}